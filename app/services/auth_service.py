async def logout(
    redis: Redis,
    user_id: uuid.UUID,
) -> None:
    """
    Invalidate a user's session by deleting their refresh token from Redis.

    Why is logout simple here?
    The access token is stateless — we cannot revoke it before its 15-minute
    expiry without a blocklist (Phase 3). For most use cases, deleting the
    refresh token is sufficient:
    - No new access tokens can be obtained after logout.
    - The last access token becomes useless in max 15 minutes.

    For high-security scenarios (e.g., "panic logout" after compromise),
    Phase 3 adds an access token blocklist using Redis Sets.

    Why no DB write on logout?
    Redis deletion is O(1) and in-memory — sub-millisecond. A DB write
    would be unnecessary overhead and would create a scalability bottleneck
    for high-frequency login/logout flows.

    Args:
        redis: Async Redis client.
        user_id: The UUID of the user logging out.
    """
    await delete_refresh_token(redis, user_id)
