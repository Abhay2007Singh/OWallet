async def login_user(
    db: AsyncSession,
    redis: Redis,
    request: LoginRequest,
) -> tuple[User, str, str]:
    """
    Authenticate a user by email and password.

    Security design:
    - We always return the same error for "email not found" and "wrong password".
      Distinguishing them would let attackers enumerate valid email addresses.
    - We use timing-safe verification even when the user isn't found
      (see verify_password_timing_safe in security.py).
    - Logging in from a new device REPLACES the existing refresh token in Redis.
      This means the previous device's session is silently invalidated.
      Multi-device sessions are a Phase 3 feature.

    Args:
        db: Async SQLAlchemy session.
        redis: Async Redis client.
        request: Validated LoginRequest schema.

    Returns:
        Tuple of (user, access_token, refresh_token).

    Raises:
        HTTPException 401: Invalid credentials (email or password incorrect).
        HTTPException 403: Account is disabled.
    """

    # -------------------------------------------------------------------------
    # Step 1: Find user by email
    # selectinload not needed here — we don't need wallets for login.
    # -------------------------------------------------------------------------
    user = await db.scalar(
        select(User).where(User.email == str(request.email))
    )

    # -------------------------------------------------------------------------
    # Step 2: Verify password (timing-safe)
    # verify_password_timing_safe() runs bcrypt even when user is None,
    # so both "user not found" and "wrong password" take ~250ms.
    # This prevents email enumeration via response timing.
    # -------------------------------------------------------------------------
    password_matches = verify_password_timing_safe(
        request.password,
        user.hashed_password if user is not None else None,
    )

    if not password_matches:
        # Generic message: don't reveal whether the email exists
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Invalid email or password",
            headers={"WWW-Authenticate": "Bearer"},
        )

    # -------------------------------------------------------------------------
    # Step 3: Check account status
    # Only after credential verification — don't reveal account existence
    # before the user proves they know the password.
    # -------------------------------------------------------------------------
    if not user.is_active:  # type: ignore[union-attr]
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="This account has been disabled. Contact support.",
        )

    # -------------------------------------------------------------------------
    # Step 4: Generate new token pair and store refresh token
    # store_refresh_token() uses SET which overwrites any existing token.
    # If the user was logged in on another device, that session is now invalid.
    # -------------------------------------------------------------------------
    access_token = create_access_token(user.id)  # type: ignore[union-attr]
    refresh_token = create_refresh_token(user.id)  # type: ignore[union-attr]
    await store_refresh_token(redis, user.id, refresh_token)  # type: ignore[union-attr]

    return user, access_token, refresh_token  # type: ignore[return-value]


