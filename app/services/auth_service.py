async def refresh_tokens(
    db: AsyncSession,
    redis: Redis,
    submitted_refresh_token: str,
) -> tuple[User, str, str]:
    """
    Validate a refresh token and issue a new token pair (rotation).

    This is the most security-critical function in the auth system.
    The rotation sequence must be:
      1. Decode JWT (validate signature + expiry)
      2. Fetch stored token from Redis
      3. Compare submitted token vs stored token → reject if mismatch
      4. DELETE the old token from Redis IMMEDIATELY (before generating new)
      5. Fetch user from DB
      6. Generate new access + refresh tokens
      7. Store new refresh token in Redis

    Why delete BEFORE generating new tokens (Step 4)?
    This is the replay attack prevention. The window between delete and
    store-new is where an attacker with the old token would fail:
      - Old token: deleted from Redis → comparison fails → 401
      - New token: not yet in Redis → comparison fails → 401
    If we stored the new token FIRST, there's a brief window where both
    old and new tokens are valid simultaneously.

    Why invalidate ALL sessions if a stale token is detected?
    If someone submits a refresh token that doesn't match Redis, it means:
    - The token was already rotated (possible replay attack)
    - OR the user manually cleared their Redis key (legitimate)
    In the replay case, an attacker is attempting to use a stolen token
    that the legitimate user already consumed. Our response: nuclear option —
    delete the current Redis key, forcing EVERYONE (including the attacker
    with a potentially valid newer token) to re-login.

    Args:
        db: Async SQLAlchemy session.
        redis: Async Redis client.
        submitted_refresh_token: The refresh JWT string from the client.

    Returns:
        Tuple of (user, new_access_token, new_refresh_token).

    Raises:
        HTTPException 401: Invalid, expired, or already-rotated token.
    """

    # -------------------------------------------------------------------------
    # Step 1: Decode the JWT (validates signature, expiry, and type claim)
    # decode_token raises HTTPException 401 if anything is wrong.
    # -------------------------------------------------------------------------
    from app.core.security import decode_token  # local import avoids circular deps

    payload = decode_token(submitted_refresh_token, expected_type="refresh")
    user_id_str: str = payload["sub"]

    try:
        user_id = uuid.UUID(user_id_str)
    except ValueError:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Could not validate credentials",
            headers={"WWW-Authenticate": "Bearer"},
        )

    # -------------------------------------------------------------------------
    # Step 2: Fetch the stored refresh token from Redis
    # -------------------------------------------------------------------------
    stored_token = await get_refresh_token(redis, user_id)

    if stored_token is None:
        # Token was deleted (logout) or Redis TTL elapsed.
        # Force re-login — we have no record of this session.
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Session not found or expired. Please log in again.",
            headers={"WWW-Authenticate": "Bearer"},
        )

    # -------------------------------------------------------------------------
    # Step 3: Compare submitted token vs stored token
    # If they don't match, this is either:
    # (a) A replay attack: someone submitting an already-rotated token
    # (b) A bug in the client sending the wrong token
    # Either way, invalidate the entire session as a security measure.
    # -------------------------------------------------------------------------
    if stored_token != submitted_refresh_token:
        # Token mismatch: possible replay attack detected.
        # Immediately invalidate ALL sessions for this user.
        await delete_refresh_token(redis, user_id)
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail=(
                "Invalid refresh token. "
                "If you did not initiate this request, your account may be compromised. "
                "Please log in again."
            ),
            headers={"WWW-Authenticate": "Bearer"},
        )

    # -------------------------------------------------------------------------
    # Step 4: Delete the old refresh token IMMEDIATELY
    # From this point, the old token is invalid. There is a brief window
    # where neither old nor new token is valid — acceptable UX trade-off
    # for correct security behaviour.
    # -------------------------------------------------------------------------
    await delete_refresh_token(redis, user_id)

    # -------------------------------------------------------------------------
    # Step 5: Fetch and validate the user from DB
    # We check DB to catch cases where the account was disabled between
    # login and this refresh attempt.
    # -------------------------------------------------------------------------
    user = await db.get(User, user_id)

    if user is None:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="User no longer exists",
            headers={"WWW-Authenticate": "Bearer"},
        )

    if not user.is_active:
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="This account has been disabled. Contact support.",
        )

    # -------------------------------------------------------------------------
    # Step 6 + 7: Generate new token pair and store the new refresh token
    # -------------------------------------------------------------------------
    new_access_token = create_access_token(user.id)
    new_refresh_token = create_refresh_token(user.id)
    await store_refresh_token(redis, user.id, new_refresh_token)

    return user, new_access_token, new_refresh_token


