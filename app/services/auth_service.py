"""
app/services/auth_service.py

Authentication business logic.

This layer sits between the route handlers (HTTP) and the database (storage).
It contains NO FastAPI imports except HTTPException — which is a domain
exception, not an HTTP transport concern. No Request, no Response, no Depends.

Why a service layer?
  - Routes are thin: they validate input, call services, return output.
  - Services are testable without HTTP: call them directly in tests.
  - If you switch from FastAPI to gRPC, only the route layer changes.

Each function follows this pattern:
  1. Business rule validation (uniqueness checks, state checks)
  2. Data transformation (hashing, token generation)
  3. Persistence (DB write + Redis write)
  4. Return domain objects (not HTTP responses)
"""

import uuid
from decimal import Decimal

from fastapi import HTTPException, status
from redis.asyncio import Redis
from sqlalchemy import select
from sqlalchemy.ext.asyncio import AsyncSession

from app.core.security import (
    create_access_token,
    create_refresh_token,
    delete_refresh_token,
    get_refresh_token,
    hash_password,
    store_refresh_token,
    verify_password_timing_safe,
)
from app.models.user import User
from app.models.wallet import Wallet, WalletCurrency
from app.schemas.auth import RegisterRequest, LoginRequest


# =============================================================================
# register_user
# =============================================================================

async def register_user(
    db: AsyncSession,
    redis: Redis,
    request: RegisterRequest,
) -> tuple[User, str, str]:
    """
    Register a new user and create their default USD wallet.

    Atomicity is critical here:
    - User creation + Wallet creation happen in ONE database transaction.
    - If wallet creation fails, the user row is rolled back automatically.
    - A user without a wallet is an invalid state in a wallet application.

    The approach:
      db.add(user) → db.flush() → db.add(wallet) → db.commit()

    Why flush() before commit()?
    flush() writes the user row to the DB within the current transaction
    (so we can access user.id for the wallet FK) but does NOT commit.
    If wallet creation raises an exception, the entire transaction rolls back —
    including the user row that was flushed.

    Security:
    - Email uniqueness is checked at the application layer for a friendly
      error message. The DB UNIQUE constraint is the final enforcement.
    - Password is hashed BEFORE the DB write — never store plaintext.
    - Registration returns tokens immediately so the client doesn't need
      to call /login separately after registration.

    Args:
        db: Async SQLAlchemy session (injected by FastAPI dependency).
        redis: Async Redis client (injected by FastAPI dependency).
        request: Validated RegisterRequest schema.

    Returns:
        Tuple of (user, access_token, refresh_token).

    Raises:
        HTTPException 409: Email already registered.
    """

    # -------------------------------------------------------------------------
    # Step 1: Check email uniqueness
    # We check at the application layer to return 409 (Conflict) with a clear
    # message. Without this, PostgreSQL would raise an IntegrityError and
    # the user would get a 500 Internal Server Error.
    # -------------------------------------------------------------------------
    existing_user = await db.scalar(
        select(User).where(User.email == str(request.email))
    )
    if existing_user is not None:
        raise HTTPException(
            status_code=status.HTTP_409_CONFLICT,
            detail="An account with this email address already exists",
        )

    # -------------------------------------------------------------------------
    # Step 2: Hash the password
    # This runs bcrypt (12 rounds, ~250ms). Done before any DB operation
    # so we don't hold a DB transaction open during the slow hashing.
    # -------------------------------------------------------------------------
    hashed_pw = hash_password(request.password)

    # -------------------------------------------------------------------------
    # Step 3: Create User and Wallet atomically
    # Both operations share the same SQLAlchemy session = same transaction.
    # -------------------------------------------------------------------------
    user = User(
        email=str(request.email),
        full_name=request.full_name,
        phone_number=request.phone_number,
        hashed_password=hashed_pw,
        # role, is_active, is_verified all use their model defaults
    )
    db.add(user)

    # flush() assigns user.id (generated by PostgreSQL) without committing.
    # We need user.id to set wallet.user_id in the next step.
    await db.flush()

    wallet = Wallet(
        user_id=user.id,
        currency=WalletCurrency.USD,
        balance=Decimal("0.00000000"),
        wallet_tag="primary",
        is_active=True,
    )
    db.add(wallet)

    # commit() finalizes BOTH the user and wallet row in one atomic write.
    # If this raises (e.g., DB unavailable), no partial data is persisted.
    await db.commit()

    # refresh() re-reads the user from DB to populate server-side defaults
    # (created_at, updated_at via server_default=func.now()).
    await db.refresh(user)

    # -------------------------------------------------------------------------
    # Step 4: Generate JWT token pair
    # -------------------------------------------------------------------------
    access_token = create_access_token(user.id)
    refresh_token = create_refresh_token(user.id)

    # -------------------------------------------------------------------------
    # Step 5: Store refresh token in Redis
    # This happens AFTER the DB commit — if Redis is down, user data is safe.
    # The user would simply need to login again (no refresh token stored).
    # -------------------------------------------------------------------------
    await store_refresh_token(redis, user.id, refresh_token)

    return user, access_token, refresh_token


